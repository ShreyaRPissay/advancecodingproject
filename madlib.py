def madlibs():
    adjective = input("Enter an adjective: ")
    noun = input("Enter a noun: ")
    verb = input("Enter a verb: ")
    adverb = input("Enter an adverb: ")
    madlibs_story = f"Once upon a time, there was a {adjective} {noun} who loved to {verb} {adverb}."
    print(madlibs_story)
madlibs()
